    private void loadDataMeja() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblManajemen.getModel();
        model.setRowCount(0);
        try (java.net.Socket socket = new java.net.Socket("localhost", 6000);
             java.io.PrintWriter out = new java.io.PrintWriter(socket.getOutputStream(), true);
             java.io.BufferedReader in = new java.io.BufferedReader(new java.io.InputStreamReader(socket.getInputStream()))) {
            out.println("GET_ALL_MEJA");
            String response;
            while ((response = in.readLine()) != null) {
                if (response.equals("DONE")) break;
                if (!response.isEmpty()) {
                    String[] data = response.split("~");
                    if (data.length >= 4) {
                        model.addRow(new Object[]{data[0], data[1], data[2], data[3]});
                    }
                }
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error load data meja: " + e.getMessage());
        }
    }
    
