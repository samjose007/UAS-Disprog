    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {
        int row = tblManajemen.getSelectedRow();
        if (row < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih meja yang akan dihapus!");
            return;
        }
        String id = tblManajemen.getValueAt(row, 0).toString();
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin hapus meja ini?", "Hapus", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try (java.net.Socket socket = new java.net.Socket("localhost", 6000);
                 java.io.PrintWriter out = new java.io.PrintWriter(socket.getOutputStream(), true);
                 java.io.BufferedReader in = new java.io.BufferedReader(new java.io.InputStreamReader(socket.getInputStream()))) {
                out.println("HAPUS_MEJA~" + id);
                String resp = in.readLine();
                javax.swing.JOptionPane.showMessageDialog(this, resp);
                loadDataMeja();
                bersihkanForm();
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {
        String noMeja = txtMeja.getText();
        String kapasitas = txtKapasitas.getText();
        String status = cmbStatus.getSelectedItem() != null ? cmbStatus.getSelectedItem().toString() : "";
        if (noMeja.isEmpty() || kapasitas.isEmpty() || status.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Lengkapi semua data!");
            return;
        }
        try (java.net.Socket socket = new java.net.Socket("localhost", 6000);
             java.io.PrintWriter out = new java.io.PrintWriter(socket.getOutputStream(), true);
             java.io.BufferedReader in = new java.io.BufferedReader(new java.io.InputStreamReader(socket.getInputStream()))) {
            out.println("TAMBAH_MEJA~" + noMeja + "~" + kapasitas + "~" + status);
            String resp = in.readLine();
            javax.swing.JOptionPane.showMessageDialog(this, resp);
            loadDataMeja();
            bersihkanForm();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {
        int row = tblManajemen.getSelectedRow();
        if (row < 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih meja yang akan diupdate!");
            return;
        }
        String id = tblManajemen.getValueAt(row, 0).toString();
        String noMeja = txtMeja.getText();
        String kapasitas = txtKapasitas.getText();
        String status = cmbStatus.getSelectedItem() != null ? cmbStatus.getSelectedItem().toString() : "";
        if (noMeja.isEmpty() || kapasitas.isEmpty() || status.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Lengkapi semua data!");
            return;
        }
        try (java.net.Socket socket = new java.net.Socket("localhost", 6000);
             java.io.PrintWriter out = new java.io.PrintWriter(socket.getOutputStream(), true);
             java.io.BufferedReader in = new java.io.BufferedReader(new java.io.InputStreamReader(socket.getInputStream()))) {
            out.println("UPDATE_MEJA~" + id + "~" + noMeja + "~" + kapasitas + "~" + status);
            String resp = in.readLine();
            javax.swing.JOptionPane.showMessageDialog(this, resp);
            loadDataMeja();
            bersihkanForm();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

