    private void loadDataProfil() {
        try {
            com.ihsganjlok.services.UserWebService_Service service = new com.ihsganjlok.services.UserWebService_Service();
            com.ihsganjlok.services.UserWebService port = service.getUserWebServicePort();
            
            String response = port.getUserById(this.currentUserId);
            if (response.startsWith("SUCCESS")) {
                String[] data = response.split("\\|");
               
                txtNama.setText(data[2]);
                txtAlamat.setText(data[3]);
                txtEmail.setText(data[4]);
                txtUsername.setText(data[5]);
                txtUsername.setEditable(false); 
                txtPassword.setText(data[6]);
                this.hakAksesSekarang = data[7];
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal memuat profil!");
        }
    }

