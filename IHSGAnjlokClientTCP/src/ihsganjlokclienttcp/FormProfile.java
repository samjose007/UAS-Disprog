    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            com.ihsganjlok.services.UserWebService_Service service = new com.ihsganjlok.services.UserWebService_Service();
            com.ihsganjlok.services.UserWebService port = service.getUserWebServicePort();
            
            String response = port.updateProfile(
                this.currentUserId,
                txtNama.getText(),
                txtAlamat.getText(),
                txtEmail.getText(),
                txtUsername.getText(),
                txtPassword.getText(),
                this.hakAksesSekarang
            );
            
            javax.swing.JOptionPane.showMessageDialog(this, response.split("\\|")[1]);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal update profil!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnLogoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogoutActionPerformed
        // TODO add your handling code here:
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin ingin logout?", "Logout", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            new FormLogin().setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnLogoutActionPerformed

    private void btnKembaliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKembaliActionPerformed
        // TODO add your handling code here:
        if (this.hakAksesSekarang.equals("Administrator")) {
            new FormMenuAdmin(this.currentUserId).setVisible(true);
        } else {
            new FormMenuCustomer(this.currentUserId).setVisible(true);
        }
        this.dispose();
    }//GEN-LAST:event_btnKembaliActionPerformed

    
